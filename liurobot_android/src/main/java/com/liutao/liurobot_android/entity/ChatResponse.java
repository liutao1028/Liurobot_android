package com.liutao.liurobot_android.entity;

public class ChatResponse {
	private String text;

	public ChatResponse() {
		super();
	}

	public ChatResponse(String text) {
		super();
		this.text = text;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
}
